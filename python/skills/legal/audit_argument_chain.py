"""
技能：论证链分析（AI辅助）

输入：判例文字 + API key
输出：事实→法条→结论之间的逻辑断裂分析 + 未证明前提 + 替代论证路径

性质：AI辅助分析维度，非硬校验。结果依赖LLM推理，多次运行输出可能不同。
依赖：DeepSeek API
"""

from ._base import 问AI, 法条提示

def 执行(判例, api_key):
    提示 = f"""请分析本案法院论证链中是否存在逻辑断裂。
请逐项分析：
1. 事实→法条的映射：法院认定的事实，与引用的法律条文之间，是否存在跳跃？事实是否完全满足法条的构成要件？
2. 法条→结论的映射：从法条推到判决结论，中间是否缺少了关键的证明步骤？
3. 未证明的前提：结论依赖但判决中未充分证明的前提假设是什么？
4. 替代论证路径：如果上述链条存在断裂，是否存在其他可适用的法条或论证路径可以弥补？
每个结论后标注(见原文第X段)。{法条提示}"""
    return 问AI(提示, 判例, api_key)
