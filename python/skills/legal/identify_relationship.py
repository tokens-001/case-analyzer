"""
技能：法律关系识别（案情模式）

输入：用户陈述的事实 + API key
输出：识别法律关系类型、主体、客体、法律依据

性质：AI辅助分析维度，非硬校验。
依赖：DeepSeek API
"""

from ._base import 问AI

def 执行(事实, api_key):
    提示 = f"""你是一名法律顾问。请根据用户陈述的事实，识别其中涉及的法律关系。

请按以下结构输出：

**1. 法律关系类型**
明确指出本案属于哪种法律关系（如民间借贷、合伙、买卖、租赁、劳动等）。

**2. 法律关系主体**
列出权利人（原告方）和义务人（被告方），以及各自主张。

**3. 法律关系客体**
本案争议指向的标的（金钱、物、行为等）。

**4. 适用法律框架**
列出可能适用的主要法律（如《民法典》合同编/侵权编、《劳动合同法》、《公司法》等），并说明为什么这些法律适用。

**5. 法律关系成立要件分析**
逐一分析该法律关系的成立要件在本案中是否满足，不满足的标注风险点。

注意：用户陈述可能不完整，基于已有事实分析，不虚构不存在的情节。"""
    return 问AI(提示, 事实, api_key)
