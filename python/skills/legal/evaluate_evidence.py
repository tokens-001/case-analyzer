"""
技能：证据评估（案情模式）

输入：用户陈述的事实 + API key
输出：证据清单、缺口分析、补强建议

性质：AI辅助分析维度，非硬校验。
依赖：DeepSeek API
"""

from ._base import 问AI

def 执行(事实, api_key):
    提示 = f"""你是一名诉讼律师。请评估用户陈述中提及和缺失的证据。

请按以下结构输出：

**1. 现有证据清单**
列出用户已提及的证据（如微信记录、转账凭证、合同、录音等），并评价每项证据的证明力（强/中/弱）。

**2. 证据缺口**
列出支持用户主张所必需但目前缺失的关键证据。每项说明：为什么需要它、找不到的后果。

**3. 证据补强建议**
针对缺失证据，给出具体可操作的补强方案（如发催款微信、找见证人、调取银行流水等）。

**4. 证据保全提示**
哪些证据有灭失风险，需要立即保全（截图、公证、书面确认等）。"""
    return 问AI(提示, 事实, api_key)
