"""
技能：核心争议提取

输入：判例文字 + API key
输出：案件的核心法律争议焦点分析文本

依赖：DeepSeek API
"""

from ._base import 问AI, 法条提示

def 执行(判例, api_key):
    提示 = f"""你是法律争议焦点分析系统。对以下判决书，提取并分类核心争议点。

## 分析要求
1. 按争议对裁判结果的影响力，从核心到次要排序
2. 每个争议点标注类型：事实争议（双方对"发生了什么"有分歧）/ 法律争议（双方对"适用什么法"有分歧）/ 混合争议
3. 每个争议点标注：原告主张→被告主张→法院采纳→归属（原告胜/被告胜/部分采纳）
4. 如法院的裁判逻辑绕过了双方主张的实质对抗，标注"法院回避实质争议"

## 输出格式
### 争议一：[争议名称]
- 类型：事实争议/法律争议/混合争议
- 原告主张：[简述]
- 被告主张：[简述]
- 法院认定：[简述]
- 归属：原告胜/被告胜/部分采纳
- 原文引用：【第X段】

### 争议二：…
{法条提示}"""
    return 问AI(提示, 判例, api_key)
