"""
技能：风险评估（案情模式）

输入：用户陈述的事实 + API key
输出：法律风险识别、等级、后果

性质：AI辅助分析维度，非硬校验。
依赖：DeepSeek API
"""

from ._base import 问AI

def 执行(事实, api_key):
    提示 = f"""你是一名诉讼律师。请根据用户陈述的事实，评估其面临的法律风险。

请按以下结构输出：

**1. 主要风险**
列出用户当前处境下最可能发生的不利法律后果，按严重程度排序。每条风险标注风险等级（高/中/低）。

**2. 对方可能的主张**
站在对方角度，分析对方可能提出的法律主张和理由。

**3. 时效风险**
检查是否涉及诉讼时效、除斥期间等时间限制问题。如有，说明剩余时间和过期后果。

**4. 举证风险**
分析举证责任分配——哪些事实需要用户举证，举证难度有多大，证据不足的后果是什么。

**5. 综合风险评级**
给出总体风险评级（高/中/低），并用一两句话说明核心理由。"""
    return 问AI(提示, 事实, api_key)
