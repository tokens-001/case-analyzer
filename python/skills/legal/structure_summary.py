"""
技能：结构化摘要（判决书模式）

输入：判决书全文 + API key
输出：当事人→案由→诉请→事实→争议→理由→结果，每信息标注原文出处
原则：事实不改写，归纳标出处。纯提取不评价。

依赖：DeepSeek API
"""

from ._base import 问AI

def 执行(判例, api_key):
    提示 = f"""你是判决书结构化摘要系统。对以下判决书，严格按照指定字段提取信息。每个字段只输出判决书原文中明确记载的内容；原文未记载的字段标注"【原文未载明】"。

## 输出格式
- 案号：[案号]（如判决书中有，必须提取；如无标注"【原文未载明】"）
- 审理法院：[法院名称]
- 当事人：原告[名称]诉被告[名称]（有第三人的补全）
- 案由：[案由名称]
- 原告诉请：1. … 2. …
- 被告诉辩：1. … 2. …
- 法院认定事实：1. … 2. …（按原文事实认定段落逐条提取）
- 争议焦点：1. … 2. …（引用判决书原文表述）
- 裁判理由：[逐点对应争议焦点，原文引用]
- 裁判结果：[判决主文原文]

## 要求
1. 每条信息末尾以【第X段】标注原文出处（按原文段落编号）
2. 不合并、不归纳、不改写原文表述
3. 判决书中长篇论述可节选关键句，以"……"表示省略，但不改变原意
4. 禁止使用"笔者认为""综上""本案"等分析性用语
5. 事实不改写优先——"长期占有""善意占有""合法占有"不可互换"""
    return 问AI(提示, 判例, api_key)
