"""
技能集：分析质量评估

包含3个技能：
- validate_analysis_quality: 基础质量验证（字数、法条引用）
- generate_risk_list: 三级风险汇总（验证+法条版本+溯源越界+法条真实性）
- compute_trust_score: 综合可信度评分（0-100分+等级+明细来源）

全部本地运行，不调API。
"""


# ═══════════════════════════════════════════════════════════
# Skill 1: 分析质量验证
# ═══════════════════════════════════════════════════════════

def validate_analysis_quality(分析1, 分析2, 分析3, 分析4, 总结, 法条统计函数):
    """基础质量检查：字数+法条引用"""
    问题列表 = []
    if len(分析1) < 20: 问题列表.append("核心争议字数过少，可能API异常")
    if len(分析2) < 20: 问题列表.append("推理链路字数过少，可能API异常")
    if len(分析3) < 20: 问题列表.append("未回答问题字数过少，可能API异常")
    if len(分析4) < 20: 问题列表.append("可平移性字数过少，可能API异常")
    if len(总结) < 50: 问题列表.append("总结字数过少")

    法条结果 = 法条统计函数(分析1, 分析2, 分析3, 分析4)
    if 法条结果 == "未引用法律条文":
        问题列表.append("四段分析均未引用法律条文")

    return {"通过": len(问题列表) == 0, "问题": 问题列表, "法条统计": 法条结果}


# ═══════════════════════════════════════════════════════════
# Skill 2: 风险列表生成
# ═══════════════════════════════════════════════════════════

def generate_risk_list(验证, 法条对照, 溯源, 法条真实性警告=None):
    """汇总四类风险：法条真实性 > 法条版本 > 溯源越界 > 验证质量"""
    风险 = []
    # P0: 法条引用真实性
    if 法条真实性警告:
        for 引用 in 法条真实性警告:
            风险.append({"等级": "danger", "内容": f"⚠️ AI引用了法条库中不存在的条文：{引用}，请人工核实该法条是否真实存在"})
    # 验证层风险
    for 问题 in 验证.get("问题", []):
        风险.append({"等级": "warning", "内容": 问题})
    # 法条版本风险
    if 法条对照:
        for item in 法条对照:
            if item.get("版本警告"):
                风险.append({"等级": "danger", "内容": item["版本警告"]})
    # 溯源越界风险
    if 溯源 and 溯源.get("items"):
        for item in 溯源["items"]:
            if not item.get("有效"):
                风险.append({"等级": "danger", "内容": f"AI编造引用：原文第{item['段号']}段不存在（共{溯源['总段落数']}段）"})
    return 风险


# ═══════════════════════════════════════════════════════════
# Skill 3: 可信度评分
# ═══════════════════════════════════════════════════════════

def compute_trust_score(验证, 法条对照, 溯源, 法条真实性警告=None):
    """综合计算分析可信度分数（0-100）+ 等级 + 来源明细"""
    基础分 = 70
    明细 = []

    # 法条引用真实性
    if 法条真实性警告:
        基础分 -= min(len(法条真实性警告) * 15, 30)
        明细.append({"来源": f"法条引用无法验证({len(法条真实性警告)}条)", "影响": -min(len(法条真实性警告) * 15, 30)})

    # 法条版本校验
    if 法条对照:
        版本警告数 = sum(1 for item in 法条对照 if item.get("版本警告"))
        if 版本警告数 == 0:
            基础分 += 15
            明细.append({"来源": "法条版本一致", "影响": +15})
        else:
            扣分 = min(版本警告数 * 10, 20)
            基础分 -= 扣分
            明细.append({"来源": f"法条版本不匹配({版本警告数}条)", "影响": -扣分})
        有效法条数 = sum(1 for item in 法条对照 if item.get("状态") == "现行有效")
        if 有效法条数 > 0:
            基础分 += 5
            明细.append({"来源": f"法条现行有效({有效法条数}条)", "影响": +5})
    else:
        基础分 -= 10
        明细.append({"来源": "无法条库对照", "影响": -10})

    # 溯源校验
    if 溯源 and 溯源.get("items"):
        越界数 = sum(1 for item in 溯源["items"] if not item.get("有效"))
        if 越界数 == 0 and 溯源.get("引用数", 0) > 0:
            基础分 += 10
            明细.append({"来源": "溯源全部验证", "影响": +10})
        elif 越界数 > 0:
            扣分 = min(越界数 * 5, 15)
            基础分 -= 扣分
            明细.append({"来源": f"溯源越界({越界数}处)", "影响": -扣分})

    # 验证层
    if 验证.get("通过"):
        基础分 += 5
        明细.append({"来源": "质量验证通过", "影响": +5})
    else:
        扣分 = len(验证.get("问题", [])) * 5
        基础分 -= 扣分
        明细.append({"来源": f"质量问题({len(验证.get('问题',[]))}项)", "影响": -扣分})

    基础分 = max(0, min(100, 基础分))
    return {"分数": 基础分, "等级": "高" if 基础分 >= 75 else "中" if 基础分 >= 50 else "低", "明细": 明细}
